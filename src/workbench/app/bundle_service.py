"""Scenario / Run bundle export + import (Phase 3 D1, plan/10 § 10.11).

A bundle is a ``tar.gz`` archive that packages a Scenario (or a Run)
plus every referenced resource so the result reproduces identically
on another PC. The MVP supports two kinds:

- ``.scnbundle`` — scenario + maps + radars + targets (the
  "share this with a colleague" use case).
- ``.runbundle`` — scenario bundle + ``run/`` directory + optional
  ``plugins/`` source (the "reproduce my paper" use case).

Both kinds share the same on-disk layout (see plan/10 § 10.11.3):

::

    bundle.scnbundle  (tar.gz)
    ├── manifest.toml                      # auto-generated bundle meta
    ├── scenario/<scenario_id>/scenario.toml
    ├── resources/maps/<map_id>/...
    ├── resources/radars/<radar_id>/...
    └── resources/targets/<targets_id>/...

The runbundle layer adds ``run/`` and (optionally) ``plugins/`` on
top. Bundle ``manifest.toml`` is auto-generated by this module and
carries:

- ``kind``  — ``"scenario"`` or ``"run"``.
- ``created_iso``  — UTC ISO-8601 timestamp.
- ``workbench_version`` — :mod:`workbench.__version__`.
- ``creator``  — free-form string (default: empty).

API surface:

- :func:`pack_scenario_bundle(source_dir, output_path, ...)`
- :func:`pack_run_bundle(source_dir, output_path, ...)`
- :func:`unpack_bundle(bundle_path, target_dir)`
- :func:`read_bundle_manifest(bundle_path)`

Both pack helpers are thin wrappers over :func:`pack_bundle` — the
shared writer that snapshots ``source_dir`` into the archive and
slips ``manifest.toml`` at the root. Sanity checks happen *before*
any tar bytes are written so a malformed source never produces a
half-written file on disk.

This module is intentionally I/O-only — no PackageManager or
ResourceLibrary coupling. Higher-level wiring (Editor "Export
Bundle..." menu) calls these primitives.
"""

from __future__ import annotations

import io
import tarfile
import tomllib
from collections.abc import Iterator
from dataclasses import dataclass, field
from datetime import UTC, datetime
from pathlib import Path
from typing import Literal

from workbench import __version__

SCENARIO_BUNDLE_SUFFIX: str = ".scnbundle"
"""Canonical extension for Scenario bundles (plan/10 § 10.11.2)."""

RUN_BUNDLE_SUFFIX: str = ".runbundle"
"""Canonical extension for Run bundles."""

BUNDLE_MANIFEST_FILENAME: str = "manifest.toml"
"""Name of the auto-generated metadata file at the archive root."""

BundleKind = Literal["scenario", "run"]
"""Bundle kind enum — drives suffix validation + manifest semantics."""


@dataclass(frozen=True, slots=True)
class BundleManifest:
    """Auto-generated bundle metadata (plan/10 § 10.11.3).

    Attributes:
        kind: ``"scenario"`` or ``"run"``.
        created_iso: UTC ISO-8601 timestamp at pack time.
        workbench_version: ``workbench.__version__`` at pack time.
        creator: Free-form author string (default empty).
        extra: Forward-compatibility dict for additional fields.
    """

    kind: BundleKind
    created_iso: str
    workbench_version: str
    creator: str = ""
    extra: dict[str, str] = field(default_factory=dict)


def pack_scenario_bundle(
    source_dir: Path | str,
    output_path: Path | str,
    *,
    creator: str = "",
) -> Path:
    """Pack ``source_dir`` into a ``.scnbundle`` (tar.gz).

    ``source_dir`` is the **bundle root** layout already arranged on
    disk (``scenario/<id>/...`` + ``resources/...``). Authors compose
    that layout via the Editor's "Export Bundle..." menu; this
    function only writes the archive + manifest.

    Args:
        source_dir: Directory to pack.
        output_path: Destination path. Must end with ``.scnbundle``.
        creator: Optional free-form author string written into
            ``manifest.toml``.

    Returns:
        Absolute :class:`Path` to the written archive.

    Raises:
        FileNotFoundError: ``source_dir`` does not exist.
        ValueError: Output path suffix is wrong.
        NotADirectoryError: ``source_dir`` is not a directory.
    """
    return pack_bundle(
        source_dir,
        output_path,
        kind="scenario",
        suffix=SCENARIO_BUNDLE_SUFFIX,
        creator=creator,
    )


def pack_run_bundle(
    source_dir: Path | str,
    output_path: Path | str,
    *,
    creator: str = "",
) -> Path:
    """Pack ``source_dir`` into a ``.runbundle`` (tar.gz).

    Same shape as :func:`pack_scenario_bundle` — only the manifest's
    ``kind`` and the output suffix change.
    """
    return pack_bundle(
        source_dir,
        output_path,
        kind="run",
        suffix=RUN_BUNDLE_SUFFIX,
        creator=creator,
    )


def pack_bundle(
    source_dir: Path | str,
    output_path: Path | str,
    *,
    kind: BundleKind,
    suffix: str,
    creator: str = "",
) -> Path:
    """Shared writer behind the two pack helpers.

    Writes a gzip-compressed tar archive containing every regular file
    under ``source_dir`` plus an auto-generated ``manifest.toml`` at
    the root. Validation (path exists / is a directory / suffix
    matches) runs *before* any bytes are written.
    """
    src = Path(source_dir).expanduser().resolve()
    out = Path(output_path).expanduser().resolve()

    if out.suffix != suffix:
        msg = f"output_path must end with {suffix!r}, got {out.name!r}"
        raise ValueError(msg)
    if not src.exists():
        msg = f"source_dir does not exist: {src}"
        raise FileNotFoundError(msg)
    if not src.is_dir():
        msg = f"source_dir must be a directory, got {src}"
        raise NotADirectoryError(msg)

    manifest = BundleManifest(
        kind=kind,
        created_iso=datetime.now(UTC).strftime("%Y-%m-%dT%H:%M:%SZ"),
        workbench_version=__version__,
        creator=creator,
    )
    manifest_blob = _serialise_manifest(manifest).encode("utf-8")

    out.parent.mkdir(parents=True, exist_ok=True)
    with tarfile.open(out, mode="w:gz") as archive:
        # Write the auto-generated manifest first so it ends up at the
        # archive root regardless of source-dir contents.
        manifest_info = tarfile.TarInfo(name=BUNDLE_MANIFEST_FILENAME)
        manifest_info.size = len(manifest_blob)
        manifest_info.mtime = int(datetime.now(UTC).timestamp())
        archive.addfile(manifest_info, io.BytesIO(manifest_blob))

        for entry in _iter_regular_files(src):
            arcname = entry.relative_to(src).as_posix()
            if arcname == BUNDLE_MANIFEST_FILENAME:
                # The source already includes a manifest.toml — skip
                # so our auto-generated one wins. plan/10 § 10.11.4
                # says the bundle manifest is always auto-generated.
                continue
            archive.add(entry, arcname=arcname, recursive=False)
    return out


def unpack_bundle(bundle_path: Path | str, target_dir: Path | str) -> Path:
    """Extract a ``.scnbundle`` / ``.runbundle`` into ``target_dir``.

    ``target_dir`` must not already exist (fail fast vs silent merge).
    Every entry's resolved path is verified inside ``target_dir``
    before extraction (zip-slip / tar-slip defence). The bundle
    manifest is parsed post-extract.

    Args:
        bundle_path: Archive to read.
        target_dir: Destination directory. Must not exist yet.

    Returns:
        Absolute :class:`Path` to ``target_dir`` after extraction.

    Raises:
        FileNotFoundError: ``bundle_path`` missing.
        FileExistsError: ``target_dir`` exists.
        ValueError: Archive missing the root ``manifest.toml`` or
            contains an entry that escapes ``target_dir``.
    """
    pkg = Path(bundle_path).expanduser().resolve()
    target = Path(target_dir).expanduser().resolve()

    if not pkg.is_file():
        msg = f"bundle archive not found: {pkg}"
        raise FileNotFoundError(msg)
    if target.exists():
        msg = f"target_dir already exists: {target}"
        raise FileExistsError(msg)

    with tarfile.open(pkg, mode="r:gz") as archive:
        names = archive.getnames()
        if BUNDLE_MANIFEST_FILENAME not in names:
            msg = f"{pkg.name} missing root {BUNDLE_MANIFEST_FILENAME}"
            raise ValueError(msg)
        for name in names:
            _reject_unsafe_path(name, target)
        target.mkdir(parents=True, exist_ok=False)
        # Python 3.14 will require an explicit filter; pin "data" filter
        # now (CVE-mitigation: rejects absolute paths, parent-relative
        # paths, special files, and modifies dangerous metadata).
        archive.extractall(target, filter="data")

    _read_manifest_blob((target / BUNDLE_MANIFEST_FILENAME).read_bytes(), source=str(pkg))
    return target


def read_bundle_manifest(bundle_path: Path | str) -> BundleManifest:
    """Read the bundle manifest without extracting the archive.

    Mirror of :func:`workbench.io.package_io.read_manifest_from_package`
    — gives the install UX a manifest preview without committing to
    a full unpack.
    """
    pkg = Path(bundle_path).expanduser().resolve()
    if not pkg.is_file():
        msg = f"bundle archive not found: {pkg}"
        raise FileNotFoundError(msg)
    with tarfile.open(pkg, mode="r:gz") as archive:
        if BUNDLE_MANIFEST_FILENAME not in archive.getnames():
            msg = f"{pkg.name} missing root {BUNDLE_MANIFEST_FILENAME}"
            raise ValueError(msg)
        member = archive.getmember(BUNDLE_MANIFEST_FILENAME)
        fileobj = archive.extractfile(member)
        if fileobj is None:
            msg = f"{pkg.name} root {BUNDLE_MANIFEST_FILENAME} is not a regular file"
            raise ValueError(msg)
        raw = fileobj.read()
    return _read_manifest_blob(raw, source=str(pkg))


def _serialise_manifest(manifest: BundleManifest) -> str:
    """Hand-rolled TOML writer for the bundle manifest.

    A full ``tomli_w`` round-trip would be nice but we don't want a
    new runtime dependency for one tiny table. The manifest schema
    is fixed (4 fields + extra dict) so a manual write is safe.
    """
    lines: list[str] = [
        f'kind = "{manifest.kind}"',
        f'created_iso = "{manifest.created_iso}"',
        f'workbench_version = "{manifest.workbench_version}"',
        f'creator = "{_escape_toml(manifest.creator)}"',
    ]
    for k, v in sorted(manifest.extra.items()):
        lines.append(f'{k} = "{_escape_toml(v)}"')
    return "\n".join(lines) + "\n"


def _escape_toml(value: str) -> str:
    """Minimal TOML basic-string escape: ``\\`` and ``"`` only."""
    return value.replace("\\", "\\\\").replace('"', '\\"')


def _read_manifest_blob(raw: bytes, *, source: str) -> BundleManifest:
    """Parse + validate the bundle manifest payload."""
    if raw.startswith(b"\xef\xbb\xbf"):
        raw = raw[3:]
    try:
        text = raw.decode("utf-8")
    except UnicodeDecodeError as exc:
        msg = f"bundle manifest in {source} is not valid UTF-8: {exc}"
        raise ValueError(msg) from exc
    try:
        data = tomllib.loads(text)
    except tomllib.TOMLDecodeError as exc:
        msg = f"bundle manifest in {source} is not valid TOML: {exc}"
        raise ValueError(msg) from exc

    kind_value = data.get("kind")
    if kind_value not in ("scenario", "run"):
        msg = f"bundle manifest kind must be 'scenario' or 'run', got {kind_value!r}"
        raise ValueError(msg)
    created = data.get("created_iso")
    workbench_version = data.get("workbench_version")
    if not isinstance(created, str) or not created:
        msg = "bundle manifest missing created_iso"
        raise ValueError(msg)
    if not isinstance(workbench_version, str) or not workbench_version:
        msg = "bundle manifest missing workbench_version"
        raise ValueError(msg)
    creator = data.get("creator", "")
    if not isinstance(creator, str):
        msg = "bundle manifest creator must be a string"
        raise ValueError(msg)
    extra: dict[str, str] = {
        str(k): str(v)
        for k, v in data.items()
        if k not in {"kind", "created_iso", "workbench_version", "creator"} and isinstance(v, str)
    }
    return BundleManifest(
        kind=kind_value,
        created_iso=created,
        workbench_version=workbench_version,
        creator=creator,
        extra=extra,
    )


def _iter_regular_files(root: Path) -> Iterator[Path]:
    """Yield every regular file under ``root`` (recursive, skip symlinks)."""
    for entry in root.rglob("*"):
        if entry.is_file() and not entry.is_symlink():
            yield entry


def _reject_unsafe_path(name: str, target: Path) -> None:
    """Reject tar entries whose resolved path escapes ``target``."""
    if name.startswith("/") or "\x00" in name:
        msg = f"tar entry has unsafe name: {name!r}"
        raise ValueError(msg)
    candidate = (target / name).resolve()
    try:
        candidate.relative_to(target)
    except ValueError as exc:
        msg = f"tar entry escapes target_dir (tar-slip): {name!r}"
        raise ValueError(msg) from exc
